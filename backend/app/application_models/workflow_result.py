from typing import List, Optional

from pydantic import BaseModel, Field

from app.analytics.models import AnalyticsResult
from app.application_models.generated_report import GeneratedReport
from app.insights.models import InsightResult
from app.intelligence.models import ObservationResult
from app.application_models.intent import IntentResult
from app.application_models.workflow_metrics import WorkflowMetrics
from app.application_models.workflow_models import QueryResult


class WorkflowResult(BaseModel):
    """Typed DTO encapsulating the full output of a completed agent workflow run.

    Returned by ReportingService and consumed exclusively by the API transport layer.
    Internal application models are never leaked beyond this boundary.
    """

    workflow_id: Optional[str] = Field(
        default=None,
        description="Identifier correlating this result to a specific workflow execution run.",
    )
    question: str = Field(
        ...,
        description="Original natural-language question submitted by the user.",
    )
    generated_sql: Optional[str] = Field(
        default=None,
        description="SQL statement generated by the LLM and validated by the safety layer.",
    )
    query_result: Optional[QueryResult] = Field(
        default=None,
        description="Structured result set returned from the database execution layer.",
    )
    generated_report: Optional[GeneratedReport] = Field(
        default=None,
        description="Narrative report DTO synthesized from the query result.",
    )
    errors: List[str] = Field(
        default_factory=list,
        description="List of non-fatal error messages recorded during workflow execution.",
    )
    metrics: Optional[WorkflowMetrics] = Field(
        default=None,
        description="Workflow execution performance metrics including per-node and LLM timings.",
    )
    intent: Optional[IntentResult] = Field(
        default=None,
        description="The classified user intent details.",
    )
    analytics: Optional[AnalyticsResult] = Field(
        default=None,
        description="Deterministic analytics computed from the executed query result.",
    )
    insights: Optional[InsightResult] = Field(
        default=None,
        description="Executive-level insight narrative grounded in the analytics result.",
    )
    observations: Optional[ObservationResult] = Field(
        default=None,
        description="Noteworthy evidence-based observations derived from analytics metadata.",
    )
    outcome: Optional[str] = Field(
        default=None,
        description="Controlled AgentOutcome value describing how the run resolved (AG-022).",
    )
